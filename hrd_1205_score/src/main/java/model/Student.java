package model;

public class Student {
	private String sid;
	private String sname;
	private String jumin;
	private String dept_name;
	private String phone;
	private String email;
	
	private String jumin_first;
	private String jumin_last;
	private String gender_code;
	private String gender_str;
	
	public String getSid() {
		return sid;
	}
	public void setSid(String sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public String getJumin() {
		return jumin;
	}
	public void setJumin(String jumin) {
		this.jumin = jumin;
	}
	public String getDept_name() {
		return dept_name;
	}
	public void setDept_name(String dept_name) {
		this.dept_name = dept_name;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getJumin_first() {
		return jumin_first;
	}
	public void setJumin_first(String jumin_first) {
		this.jumin_first = jumin_first;
	}
	public String getJumin_last() {
		return jumin_last;
	}
	public void setJumin_last(String jumin_last) {
		this.jumin_last = jumin_last;
	}
	public String getGender_code() {
		return gender_code;
	}
	public void setGender_code(String gender_code) {
		this.gender_code = gender_code;
	}
	public String getGender_str() {
		return gender_str;
	}
	public void setGender_str(String gender_str) {
		this.gender_str = gender_str;
	}
	
}
